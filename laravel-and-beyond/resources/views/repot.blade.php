        <h2>Why should you repot?</h2>
        <h3>1. To let your plant grow</h3>
        <p>
            If your plant is still in its original pot (in which it was grown), it is a good idea to repot your plant
            immediately
            after buying it or else the following spring after purchase.
        </p><br>
        <p>
            There is not a lot of room for the roots in that small pot. In order for your plant to grow, it's important that
            the
            roots have space to grow too.
        </p><br>
        <p>
            With most houseplants you can easily see whether your plant needs more space. Pry the root ball out of the pot.
            If you see roots all around and at the bottom of the root ball, your plant definitely needs a bigger pot.
        </p><br>

        <h3>2. The plant can hold more water</h3>
        <p>
            The cultivation pot (that plastic pot in which you buy your plant) contains very little soil,
            and that is especially the case if the plant has already grown a lot and roots have taken over most of the
            space.
            When you water your plant, the earth retains the water and the roots slowly withdraw the water from the earth.
            But the less soil, the less water can be retained and the faster the water runs out.
            With small root balls you should therefore give more water. A larger root ball (so more soil around the roots)
            holds
            more water,
            which means that you have to water your houseplant a little less frequently. More time for other things!
        </p><br>

        <h3>3. To add fresh fertilizer</h3>
        <p>
            After about three months, the average houseplant has used up all the food from the potting soil. In addition to
            water, the plant needs all kinds of building blocks to live and grow: potassium, phosphorus, and so on. All
            those
            building materials have been added to the potting soil in which you buy your plant. And all those building
            materials
            are also contained in good fresh potting soil. Of course you add extra (in spring and summer) and that is also
            sufficient. But a dash of fresh potting soil is wonderful for a plant!
        </p><br>
        <p>
            Ofcourse it's possible you don't want to repot (maybe you don't want your plant to grow bigger).
            Then you can also scrape off the top layer of old potting soil and replace it with new potting soil.
        </p><br>

        <h3>4. To put the roots back into a good texture.</h3>
        <p>
            Soil will get old. The longer the soil is in the pot, the less perfect the plant becomes.
            The nutrition disappears but that can be fixed by adding plant food.
            But the texture of the potting soil is also diminishing. Potting soil must be nice and airy,
            because soggy/muddy soil does not allow oxygen to penetrate, which causes the roots to rot. At the same time,
            the
            potting
            soil should not be too airy, because roots cannot naturally draw water and building materials from air. In
            short: it
            is a delicate balance, and after about two to three years that delicate balance of your potting soil is a lot
            less
            delicate.
        </p><br>
        <p>
            So give your plants fresh potting soil once every two to three years, even if you keep your plants in the same
            pot.
            First let the root ball dry out a bit, then remove the root ball from the pot, shake it out,
            pick as much soil as possible from between the roots and then add the fresh potting soil.
        </p><br>
        <img src="https://image.shutterstock.com/image-photo/repotting-green-plants-red-old-600w-1032599953.jpg"
            alt="plant pots on wood stump">

        <h2>When should you repot?</h2>
        <p>In spring ofcourse!</p><br>
        <p>
            It is best to repot house plants in spring, from mid-March. The growing season is upon us so the plant is
            growing
            again.
            This means firstly that the plant may need more space for its roots for the coming season and
            secondly that roots that may get damaged during the repotting recover quickly.
        </p><br>

        <h2>How should you repot?</h2>
        <p class="bold">Repot to a larger pot.</p><br>
        <p>
            If you want your plant to grow bigger, repot to a pot with a diameter about 20% larger than the pot you had it
            in at
            the time of repotting.
        </p><br>
        <p>
            Make sure the soil reaches till 1 cm below the rim of the pot. If the soil comes all the way to the edge of the
            pot,
            it will be difficult to water. Then the water gushes out of the pot and your house becomes a puddle of mud.
            Fill the bottom with soil, carefully pry the plant out of the pot (it is best to pick at the roots if
            necessary),
            place the root ball on the layer of soil and fill in the space around the root ball.
        </p><br>
        <img src="https://image.shutterstock.com/image-vector/transplanting-potted-flower-steps-vector-600w-1747090523.jpg"
            alt="How to repot a plant, step by step">

        <p class="bold">Repot to the same size</p><br>
        <p>
            Don't want your plant to grow taller, or has your plant already reached its maximum size?
            Then your houseplant can simply remain in the pot it is in. But even then you want to repot once every two to
            three
            years.
            Because the potting soil slowly loses its airy and moisture-absorbing structure,
            which is so important to be able to serve your houseplant well.
        </p><br>
        <p>
            In this case, let your houseplant dry out a bit before repotting. Remove the plant with root ball from your pot
            and
            shake out the root ball as much as possible. Remove as much old soil from between the roots as possible.
            Then put your plant back in the soil with fresh potting soil. Then water it.
        </p><br>
        <p>Congrats, you just turned into a repotting expert!</p>
